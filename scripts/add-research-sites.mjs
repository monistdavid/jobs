const EMPTY_ARRAYS = ['addresses', 'siteTypes', 'populations', 'services', 'deliveryModes', 'languages', 'applicationRequirements'];

function buildRecord([name, website, area, paid, compensationNote, phone, description, extras = {}]) {
  const rec = {
    id: slugify(name),
    name,
    website,
    description,
    area,
    addresses: [],
    siteTypes: extras.siteTypes || [],
    populations: extras.populations || [],
    services: extras.services || [],
    deliveryModes: [],
    paid,
    compensationNote,
    languages: extras.languages || [],
    positionsCount: '',
    applicationRequirements: [],
    deadline: '',
    applyMethod: 'See website',
    contact: '',
    phone: phone || '',
    coverLetter: extras.coverLetter || 'unknown', // 'required' | 'not-required' | 'unknown'
    hiresAfter: false,
    source: 'research',
  };
  for (const k of EMPTY_ARRAYS) if (!Array.isArray(rec[k])) rec[k] = [];
  return rec;
}

const existing = JSON.parse(readFileSync('data/sites.json', 'utf8'));
const seed = existing.filter((s) => s.source !== 'research');
const seedIds = new Set(seed.map((s) => s.id));

const research = [];
const ids = new Set();
for (const row of RAW) {
  const rec = buildRecord(row);
  if (seedIds.has(rec.id)) { console.warn(`SKIP duplicate of seed: ${rec.name}`); continue; }
  if (ids.has(rec.id)) { console.warn(`SKIP duplicate research id: ${rec.name}`); continue; }
  ids.add(rec.id);
  research.push(rec);
}

const merged = [...seed, ...research];
writeFileSync('data/sites.json', JSON.stringify(merged, null, 2) + '\n');
console.log(`seed: ${seed.length}, research added: ${research.length}, total: ${merged.length}`);
